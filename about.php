    <script type="text/javascript">
        $(document).ready(function () {
            
            // check if user is on monile
            var isMobile = {
                Android: function () {
                    return navigator.userAgent.match(/Android/i);
                },
                BlackBerry: function () {
                    return navigator.userAgent.match(/BlackBerry/i);
                },
                iOS: function () {
                    return navigator.userAgent.match(/iPhone|iPad|iPod/i);
                },
                Opera: function () {
                    return navigator.userAgent.match(/Opera Mini/i);
                },
                Windows: function () {
                    return navigator.userAgent.match(/IEMobile/i) || navigator.userAgent.match(/WPDesktop/i);
                },
                any: function () {
                    return (isMobile.Android() || isMobile.BlackBerry() || isMobile.iOS() || isMobile.Opera() || isMobile.Windows());
                }
            };
            
            if (isMobile.any()) {
                $('.navbar-left li').last().remove();
                $('.section').addClass('pp-scrollable');

                // change the original navbar
                var newSrc = 'img/logo-white.png';
                var image = $('.navbar-brand img');

                $('.default-navbar').addClass('scrolled').find('.navbar-brand img');
                image.attr('src', newSrc);
            } else {
                $(window).on('scroll', function (e) {
                    var scrollPos = $(document).scrollTop();

                    if (scrollPos >= 150) {
                        $('.default-navbar').animate({ opacity: 1 });

                        // change the original navbar
                        var newSrc = 'img/logo-white.png';
                        var image = $('.navbar-brand img');

                        $('.default-navbar').addClass('scrolled').find('.navbar-brand img');
                        image.attr('src', newSrc);
                    } else {
                        // set the animation for navbar
                        $('.default-navbar').css({ opacity: 0 });

                        // back to original navbar
                        var oldSrc = 'img/logo-teal.png';
                        var image = $('.navbar-brand img');

                        $('.default-navbar').removeClass('scrolled').find('.navbar-brand img');
                        image.attr('src', oldSrc);
                    }
                });
            }

            var owl = $('.owl-carousel');

            owl.owlCarousel({
                rtl: true,
                items: 1,
                loop: false,
                margin: 30,
                nav: false,
                navContainer: '#owl-nav',
                dotsContainer: '#owl-dots',
                onInitialized: onInitWithResized(),
                responsive: {
                    768: {
                        items: 2
                    }
                }
            });

            function onInitWithResized() {
                setTimeout(function () {
                    var itemWidth = (owl.find('.item').width() + 80) / 2;
                    $('#owl-nav').css({ 'width': itemWidth + 'px' });
                    $('.owl-next').html('<span class="fa fa-fw fa-long-arrow-right"></span>');
                    $('.owl-prev').html('<span class="fa fa-fw fa-long-arrow-left"></span>');
                }, 500);
            }

            // navbar li class on hover
            $('.navbar-left li').hover(
                function () {
                    $(this).nextAll('li').prevAll('li').removeClass('hover');
                    $(this).addClass('hover');
                }, function () {
                    $(this).addClass('hover');
                    $(this).nextAll('li').prevAll('li').removeClass('hover');
                }
            );

        });
    </script>

